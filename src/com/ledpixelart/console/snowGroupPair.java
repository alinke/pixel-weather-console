package com.ledpixelart.console;

public class snowGroupPair {
	
	    private String x = "";
	    private String y = "";

	    public snowGroupPair(String x, String y)
	    {
	        this.x = x;
	        this.y = y;
	    }

	    public snowGroupPair()
	    {

	    }

	    public String getX() {
	        return x;
	    }

	    public void setX(String x) {
	        this.x = x;
	    }

	    public String getY() {
	        return y;
	    }

	    public void setY(String y) {
	        this.y = y;
	    }
	
}
